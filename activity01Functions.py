# Write 2 Python functions to:
# show the list content.
# find the max value in the list.

myList = [10, 2, 3, 4, 7]

def listContent(List):
    print("The content of the list is: ", List)

def maxValue(List):
    print("The max value in the list is: ", max(List))

listContent(myList)
maxValue(myList)
